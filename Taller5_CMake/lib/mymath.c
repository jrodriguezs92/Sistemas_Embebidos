#include "mymath.h"

double sum(double a, double b) {
    return a+b;
}

double subs(double a, double b) {
    return a-b;
}

double mult(double a, double b) {
    return a*b;
}

double div(double a, double b) {
    return a/b;
}

double sqroot(double a) {
    return sqrt(a);
}
